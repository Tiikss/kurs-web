const bankState=document.getElementById('bank-switch');
const powerState=document.getElementById('power-switch');
const textField=document.getElementById('text-field');
const sliderVol=document.querySelector('.slider');
const volVal=document.getElementById('vol-value');

document.addEventListener('keydown', function(e) {
    if(!powerState.checked)
        return;

    const keyCode=e.keyCode;

    function playSound(bank){
        const soundObject=bank.find((sound)=>sound.keyCode===keyCode);

        if(soundObject){
            const audio=new Audio(soundObject.url);
            audio.volume=sliderVol.value*0.01;
            volVal.innerText=sliderVol.value;
            audio.play();
        }
    }

    if(bankState.checked){
        playSound(bankOne);
        textField.innerHTML=bankOne.find((sound)=>sound.keyCode===keyCode).id;
    }
    else{
        playSound(bankTwo);
        textField.innerHTML=bankTwo.find((sound)=>sound.keyCode===keyCode).id;
    }
})

